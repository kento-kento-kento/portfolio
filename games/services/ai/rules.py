GAME_RULES ='''

【ゲーム概要】
・10×10 の固定グリッド上でブロックを配置するリアルタイム型パズルゲーム
・制限時間は 180 秒（カウントダウン後に開始）
・ゲーム中は常に「手持ちブロック3個」が提示される
・3個すべて配置し終えると、新しい3個が自動で補充される
・配置できない状態になっても即ゲームオーバーにはならず、時間切れで終了する

【操作仕様（重要）】
・数字キー 1 / 2 / 3 で手持ちブロックを選択
・W A S D で盤面カーソルを移動
・Space キーで時計回りに90度回転（回転制限なし）
・Enter キーで現在位置に配置
・ブロックは盤面外・重なりがある場合は配置不可
・配置可能かどうかは常に判定されている（置けない場所では赤表示）

【盤面ルール】
・ブロックは「左上基準」で配置される
・回転後の形状サイズに応じて、カーソル移動範囲が自動制限される
・盤面は完全にフラットで、落下や重力の概念は存在しない

【消去ルール】
・1行または1列がすべて埋まると、その行・列は即時消去される
・行と列は同時に複数消去されることがある
・消去後、ブロックは詰め直されず、そのまま空白になる

【スコア仕様（重要）】
・ブロックを1回配置するごとに +200 点
・同時に消去した行＋列の合計数に応じてボーナス加算：
    1 本：+1000
    2 本：+3000
    3 本：+5000
    4 本：+7000
    5 本以上：+10000（固定）
・大量消去は短時間でスコアを大きく伸ばせるが、盤面管理が難しくなる

【ブロック生成ルール】
・通常ブロックは以下を含む：
    ・1×1、1×3、1×4
    ・2×2、3×2
    ・L字・逆L字
    ・十字型、Z字、S字
・スコアが 15000 点以上になると、変則ブロックが追加される：
    ・1×5、5×1 の超長ブロック
    ・L字拡張型や特殊な凹型ブロック
・変則ブロックは盤面要求が非常に厳しく、配置ミスが致命傷になりやすい

【ゲーム性の特徴（体感ルール）】
・序盤は盤面が広く、ほぼすべてのブロックが安全に配置できる
・中盤以降は「置けるが将来詰む配置」が増え、判断力が問われる
・縦長ブロック（1×4、1×5）は列消去に非常に有効だが、
  使いどころを誤ると一気に盤面を破壊する危険性が高い
・中央を埋めすぎると大型・変則ブロックの逃げ場がなくなりやすい
・端だけを使いすぎても消去ラインを作りづらくなるため、
  中央と外周のバランスが重要

【上級者視点の注意点】
・「今置ける」より「次の3手で詰まない」配置が重要
・縦長ブロックは温存できる唯一の強力な消去トリガー
・中央を完全に空けすぎると、逆に消去チャンスを失うことがある
・大量消去を狙うほど、次ターンの難易度が跳ね上がる

このルールを前提として、プレイ内容を分析してください。
'''